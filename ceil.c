#include<stdio.h>
#include<math.h>
int main()
{
	printf("%f",ceil(4.0));
	printf("\n%f",floor(4.9));
	printf("\n%d",abs(-20.25));
	printf("\n%f",fabs(-20.25));//floor abs
	printf("\n%f",sqrt(25));//no negative
	printf("\n%f",pow(3,2));
	printf("\n%f",log(2));
	printf("\n%f",log10(2));
	printf("\n%f",exp(2));
	printf("\n%f",sin(30));
	printf("\n%f",cos(30));
	printf("\n%f",tan(30));
	printf("\n%f",round(4.2));
	
}

